<?php

    // Faça duas variáveis, uma com o valor 5 e outra com o valor 3.
    // Teste os operadores: ==, !=, ===, !==.

    $a = 5;
    $b = 3;

    if ($a == $b) {
        echo "A é igual a B <br>";
    }

    if ($a != $b) {
        echo "A é diferente de B <br>";
    }

    if ($a === $b) {
        echo "A é idêntico a B <br>";
    }

    if ($a !== $b) {
        echo "A não é idêntico a B <br>";
    }

?>