<?php

    $x = 0;

    while ($x < 10) {
        echo $x . "<br>";

        $x ++;
    }

    echo "Continuando código <br>";

    $y = 10;

    while ($y > 0) {
        echo $y . "<br>";

        $y --;
    }

    echo "Continuando código";

    $z = 20;

    while ($z > 0) {

        if ($z % 2 != 0) {
            echo $z . "<br>";
        }

        $z --;

    }


?>